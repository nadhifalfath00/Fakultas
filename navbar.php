<script>
/*=============== SHOW MENU navbar start ===============*/
const showMenu = (toggleId, navId) =>{
   const toggle = document.getElementById(toggleId),
         nav = document.getElementById(navId)

   toggle.addEventListener('click', () =>{
       // Add ft-show-menu class to nav menu
       nav.classList.toggle('ft-show-menu')

       // Add ft-show-icon to show and hide the menu icon
       toggle.classList.toggle('ft-show-icon')
   })
}

showMenu('ft-nav-toggle','ft-nav-menu')
/*=============== SHOW MENU navbar end ===============*/
</script>